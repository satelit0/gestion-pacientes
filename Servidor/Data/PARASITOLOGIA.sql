CREATE TABLE PARASITOLOGIA(
ID_PARASITOLOGIA INT IDENTITY(1,1),
ID_RECORDS_P VARCHAR(7),
ELEMENTOSPARASITOS VARCHAR(1),
ENTAMOEBAHISTOLICA VARCHAR(15),
GIARDIALAMBIA VARCHAR(15),
HYMENOLEPSIS VARCHAR(15),
ASCARISLUMBRICOIDE VARCHAR(15),
TRICHURIS VARCHAR(15),
UNCINARIAS VARCHAR(15),
OTROS_NOMBRE VARCHAR(10),
OTROS_DESCRIBA VARCHAR(15),
PRIMARY KEY(ID_RECORDS_P)
)
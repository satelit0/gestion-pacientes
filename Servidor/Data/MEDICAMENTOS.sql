CREATE TABLE MEDICAMENTOS(
IDMEDICAMENTO INT IDENTITY (1,1),
NOMBRE_MEDICAMENTO VARCHAR(50) NOT NULL,
DESCRIPCION_MEDICAMENTO VARCHAR(60),
CANTIDAD_COMPUESTO VARCHAR(5),
UNIDAD_MEDIDA VARCHAR(10),
MODO_CONSUMO VARCHAR(25),
NOTA VARCHAR(60),
FECHA_CREADO_MEDICAMENTO DATE,
PRIMARY KEY(IDMEDICAMENTO)
)